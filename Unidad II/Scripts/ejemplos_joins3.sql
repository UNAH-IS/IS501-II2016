--UTILIZANDO PRODUCTOS CARTESIANOS Y WHERE
SELECT *
FROM TBL_RUTAS;

SELECT *
FROM TBL_PUNTOS_X_RUTAS;

SELECT *
FROM TBL_PUNTO_ESCALA;

SELECT  A.CODIGO_VUELO,
        A.FECHA_HORA_PARTIDA,
        A.FECHA_HORA_LLEGADA,
        A.CODIGO_AVION,
        A.CODIGO_RUTA,
        B.ALIAS_RUTA,
        B.TIEMPO_PROMEDIO_MINUTOS,
        C.CODIGO_PUNTO,
        D.NOMBRE_PUNTO,
        D.LATITUD,
        D.LONGITUD,
        C.ORDEN_RUTA
FROM  TBL_VUELOS A,
      TBL_RUTAS B,
      TBL_PUNTOS_X_RUTAS C,
      TBL_PUNTO_ESCALA D
WHERE A.CODIGO_RUTA = B.CODIGO_RUTA
AND   B.CODIGO_RUTA = C.CODIGO_RUTA
AND   C.CODIGO_PUNTO = D.CODIGO_PUNTO
ORDER BY A.CODIGO_VUELO, C.ORDEN_RUTA;
