-----------------
SELECT A.CODIGO_VUELO,
      A.FECHA_HORA_PARTIDA,
      A.FECHA_HORA_LLEGADA,
      A.CODIGO_AVION,
      B.REGISTRO,
      B.CANTIDAD_ASIENTOS,
      C.NOMBRE_MARCA,
      D.NOMBRE_MODELO,
      A.CODIGO_RUTA,
      E.CODIGO_TRIPULANTE,
      F.NOMBRE
FROM TBL_VUELOS A
INNER JOIN TBL_AVIONES B
ON (A.CODIGO_AVION = B.CODIGO_AVION)
INNER JOIN TBL_MARCAS C
ON (B.CODIGO_MARCA = C.CODIGO_MARCA)
INNER JOIN TBL_MODELOS D
ON (B.CODIGO_MODELO = D.CODIGO_MODELO)
INNER JOIN TBL_TRIPULACION_X_VUELOS E
ON (A.CODIGO_VUELO = E.CODIGO_VUELO)
INNER JOIN TBL_TRIPULACION F
ON (E.CODIGO_TRIPULANTE = F.CODIGO_TRIPULANTE)
ORDER BY A.CODIGO_VUELO;



