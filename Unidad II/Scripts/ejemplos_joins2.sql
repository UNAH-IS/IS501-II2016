SELECT A.EMPLOYEE_ID,
      A.FIRST_NAME || ' ' ||A.LAST_NAME AS EMPLOYEE_NAME,
      A.EMAIL,
      A.PHONE_NUMBER,
      A.HIRE_DATE,
      A.JOB_ID,
      B.JOB_TITLE,
      A.SALARY,
      A.COMMISSION_PCT,
      A.MANAGER_ID,
      A.DEPARTMENT_ID,
      C.FIRST_NAME ||' '|| C.LAST_NAME AS MANAGER_NAME,
      D.DEPARTMENT_NAME,
      E.CITY,
      E.STREET_ADDRESS,
      F.COUNTRY_NAME,
      G.REGION_NAME
FROM  EMPLOYEES A,
      JOBS B,
      EMPLOYEES C,
      DEPARTMENTS D,
      LOCATIONS E,
      COUNTRIES F,
      REGIONS G
WHERE A.JOB_ID = B.JOB_ID
AND  A.MANAGER_ID = C.EMPLOYEE_ID(+)
AND  A.DEPARTMENT_ID = D.DEPARTMENT_ID(+)
AND  D.LOCATION_ID = E.LOCATION_ID(+)
AND  E.COUNTRY_ID = F.COUNTRY_ID(+)
AND  F.REGION_ID = G.REGION_ID(+);


